<template>
  <span class="cc-toggle-img" @click="toggle">
    <cc-img :img="img" :width="width" :height="height"
            :size="size" :unit="unit"/></span>
</template>

<script>

  // Components
  const ccImg = require('components/general/ccImg.vue');

  module.exports = {
    name: 'cc-toggle-img',
    components: {
      ccImg,
    },
    extends: ccImg,
    props: {
      value: {
        type: Boolean,
        default: null,
      },
      default: Boolean,
      disabled: Boolean,
    },
    data() {
      return {};
    },
    computed: {
      toggleValue: {
        set($newVal) {
          this.$emit('input', $newVal);
        },
        get() {
          return this.value !== null ? (this.value === true) : (this.default === true);
        },
      },
    },
    methods: {
      toggle() {
        if (this.disabled === true) {
          return;
        }
        this.toggleValue = !this.toggleValue;
      },
    },
  };
</script>
